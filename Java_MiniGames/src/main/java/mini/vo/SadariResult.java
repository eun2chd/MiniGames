package mini.vo;

public class SadariResult {
	
	private int round;
	
	private String LR;
	
	private String line;
	
	private String oven;
	
	private int Xpoint;
	
	private int Ypoint;
	

	public int getRound() {
		return round;
	}

	public void setRound(int round) {
		this.round = round;
	}

	public String getLR() {
		return LR;
	}

	public void setLR(String lR) {
		LR = lR;
	}

	public String getLine() {
		return line;
	}

	public void setLine(String line) {
		this.line = line;
	}

	public String getOven() {
		return oven;
	}

	public void setOven(String oven) {
		this.oven = oven;
	}

	public int getXpoint() {
		return Xpoint;
	}

	public void setXpoint(int xpoint) {
		Xpoint = xpoint;
	}

	public int getYpoint() {
		return Ypoint;
	}

	public void setYpoint(int ypoint) {
		Ypoint = ypoint;
	}
	

}
