package mini.vo;

public class userInfo {
	
	// 사용자 아아디
	private String userid = ""; 
	
	// 사용자 패스워드
	private String Password = "";
	

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getPassword() {
		return Password;
	}

	public void setPassword(String password) {
		Password = password;
	}
	

	
}
